-- service role access for new terms tables
do $$ begin
  alter table public.terms_versions enable row level security;
  alter table public.user_consents enable row level security;
  drop policy if exists service_role_all on public.terms_versions;
  drop policy if exists service_role_all on public.user_consents;
  create policy service_role_all on public.terms_versions for all using (auth.role()='service_role') with check (auth.role()='service_role');
  create policy service_role_all on public.user_consents for all using (auth.role()='service_role') with check (auth.role()='service_role');
end $$;
