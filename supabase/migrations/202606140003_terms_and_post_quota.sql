create table if not exists public.terms_versions (
  id bigint generated by default as identity primary key,
  type text not null check (type in ('terms','privacy','candidate_consent','company_policy','refund_policy','marketing')),
  version text not null,
  title text not null,
  content_url text,
  is_required boolean not null default true,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  unique(type, version)
);

create table if not exists public.user_consents (
  id bigint generated by default as identity primary key,
  user_email text not null,
  user_role text not null check (user_role in ('user','company','admin')),
  consent_type text not null,
  version text not null,
  ip text,
  user_agent text,
  accepted_at timestamptz not null default now(),
  metadata jsonb not null default '{}'::jsonb,
  unique(user_email, consent_type, version)
);

