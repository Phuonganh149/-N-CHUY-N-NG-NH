alter table public.company_subscriptions add column if not exists posts_limit integer;
update public.company_subscriptions set posts_limit = coalesce(posts_limit, post_limit, 0) where posts_limit is null;

create or replace function public.reserve_company_job_slot(p_company_id bigint, p_actor_email text)
returns public.company_subscriptions
language plpgsql security definer set search_path=public as $$
declare v_sub public.company_subscriptions%rowtype;
begin
  select * into v_sub
  from public.company_subscriptions
  where "companyId" = p_company_id
    and status = 'active'
    and (ends_at is null or ends_at >= now())
    and coalesce(posts_used,0) < coalesce(posts_limit, post_limit, 0)
  order by ends_at asc nulls last, id asc
  limit 1
  for update;

  if not found then
    raise exception 'Không còn gói đăng tuyển hợp lệ hoặc đã hết lượt đăng tin.';
  end if;

  update public.company_subscriptions
  set posts_used = coalesce(posts_used,0) + 1, updated_at = now()
  where id = v_sub.id
  returning * into v_sub;

  insert into public.audit_logs("actorEmail","actorRole",action,"entityType","entityId",metadata)
  values(p_actor_email,'company','reserve_job_slot','company_subscription',v_sub.id::text,jsonb_build_object('companyId',p_company_id,'postsUsed',v_sub.posts_used,'postsLimit',coalesce(v_sub.posts_limit,v_sub.post_limit)));

  return v_sub;
end $$;

create or replace function public.release_company_job_slot(p_subscription_id bigint, p_actor_email text)
returns public.company_subscriptions
language plpgsql security definer set search_path=public as $$
declare v_sub public.company_subscriptions%rowtype;
begin
  select * into v_sub from public.company_subscriptions where id = p_subscription_id for update;
  if not found then raise exception 'Không tìm thấy subscription.'; end if;
  update public.company_subscriptions
  set posts_used = greatest(0, coalesce(posts_used,0)-1), updated_at = now()
  where id = p_subscription_id returning * into v_sub;
  insert into public.audit_logs("actorEmail","actorRole",action,"entityType","entityId",metadata)
  values(p_actor_email,'system','release_job_slot','company_subscription',v_sub.id::text,jsonb_build_object('postsUsed',v_sub.posts_used));
  return v_sub;
end $$;

